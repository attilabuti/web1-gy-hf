function _initPage() {
    const regForm = document.querySelector('#contact');
    if (regForm !== null && regForm !== undefined) {
        regForm.addEventListener('submit', checkContactForm);
    }
}

function initMap() {
    new google.maps.Map(document.getElementById('map'), {
        styles: darkStyle,
        center: { lat: 48.85, lng: 2.35 },
        zoom: 12,
        disableDefaultUI: true,
        draggable: false,
        scrollwheel: false,
        disableDoubleClickZoom: true,
        keyboardShortcuts: false
    });
}

function checkContactForm(e) {
    e.preventDefault();

    const result = validate.check([{
        el: document.querySelector('#subject'),
        rules: [
            { rule: 'required', message: 'A tárgy megadása kötelező.' },
            { rule: 'max:120',  message: 'A tárgy maximum $$ karakter hosszú lehet.' },
        ]
    }, {
        el: document.querySelector('#message'),
        rules: [
            { rule: 'required', message: 'Az üzenet megadása kötelező.' },
            { rule: 'max:5000', message: 'Az üzenet maximum $$ karakter hosszú lehet.' },
        ]
    }]);

    if (result) {
        document.getElementById('contact').submit();
    }
}

